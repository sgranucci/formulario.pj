      <div class="modal-body">
        <p>

(Artículo 20 de la Ley 25.246 de la Unidad de Información Financiera)<br>
https://www.argentina.gob.ar/normativa/nacional/ley-25246-62977/texto<br><br>

1. Las entidades financieras sujetas al régimen de la ley 21.526 y modificatorias.<br>
2. Las entidades sujetas al régimen de la ley 18.924 y modificatorias y las personas humanas o jurídicas autorizadas por el Banco Central de la República Argentina para operar en la compraventa de divisas bajo forma de dinero o de cheques extendidos en divisas o mediante el uso de tarjetas de crédito o pago, o en la transmisión de fondos dentro y fuera del territorio nacional.<br>
3. Las personas humanas o jurídicas que como actividad habitual exploten juegos de azar.<br>
4. Personas humanas y/o jurídicas registradas ante la Comisión Nacional de Valores para actuar como intermediarios en mercados autorizados por la citada comisión y aquellos que actúen en la colocación de Fondos Comunes de Inversión o de otros productos de inversión colectiva autorizados por dicho organismo. <br>
5. Personas jurídicas autorizadas por la Comisión Nacional de Valores para actuar en el marco de sistemas de financiamiento colectivo a través del uso de portales web u otros medios análogos y demás personas jurídicas registradas en el citado organismo a cargo de la apertura del legajo e identificación del perfil del cliente para invertir en el ámbito del mercado de capitales.<br>
6. Los registros públicos de comercio, los organismos representativos de fiscalización y control de personas jurídicas, los registros de la propiedad inmueble, los registros de la propiedad automotor, los registros prendarios, los registros de embarcaciones de todo tipo y los registros de aeronaves.<br>
7. Las personas humanas o jurídicas dedicadas a la compraventa de obras de arte, antigüedades u otros bienes suntuarios, inversión filatélica o numismática, o a la exportación, importación, elaboración o industralización de joyas o bienes con metales o piedras preciosas.<br>
8. Las empresas aseguradoras.<br>
9. Las empresas emisoras de cheques de viajero u operadoras de tarjetas de crédito o de compra.<br>
10. Las empresas dedicadas al transporte de caudales.<br>
11. Las empresas prestatarias o concesionarias de servicios postales que realicen operaciones de giros de divisas o de traslado de distintos tipos de moneda o billete.<br>
12. Los escribanos públicos.<br>
13. Las entidades comprendidas en el artículo 9º de la ley 22.315.<br>
14. Los despachantes de aduana definidos en el artículo 36 y concordantes del Código Aduanero (ley 22.415 y modificatorias).<br>
15. Los organismos de la Administración Pública y entidades descentralizadas y/o autárquicas que ejercen funciones regulatorias, de control, supervisión y/o superintendencia sobre actividades económicas y/o negocios jurídicos y/o sobre sujetos de derecho, individuales o colectivos: el Banco Central de la República Argentina, la Administración Federal de Ingresos Públicos, la Superintendencia de Seguros de la Nación, la Comisión Nacional de Valores, la Inspección General de Justicia, el Instituto Nacional de Asociativismo y Economía Social y el Tribunal Nacional de Defensa de la Competencia;<br>
16. Los productores, asesores de seguros, agentes, intermediarios, peritos y liquidadores de seguros cuyas actividades estén regidas por las leyes 20.091 y 22.400, sus modificatorias, concordantes y complementarias;<br>
17. Los profesionales matriculados cuyas actividades estén reguladas por los consejos profesionales de ciencias económicas;<br>
18. Igualmente están obligados al deber de informar todas las personas jurídicas que reciben donaciones o aportes de terceros;<br>
19. Los agentes o corredores inmobiliarios matriculados y las sociedades de cualquier tipo que tengan por objeto el corretaje inmobiliario, integradas y/o administradas exclusivamente por agentes o corredores inmobiliarios matriculados;<br>
20. Las asociaciones mutuales y cooperativas reguladas por las leyes 20.321 y 20.337 respectivamente;<br>
21. Las personas humanas o jurídicas cuya actividad habitual sea la compraventa de automóviles, camiones, motos, ómnibus y microómnibus, tractores, maquinaria agrícola y vial, naves, yates y similares, aeronaves y aerodinos.<br>
22. Las personas humanas o jurídicas que actúen como fiduciarios, en cualquier tipo de fideicomiso y las personas humanas o jurídicas titulares de o vinculadas, directa o indirectamente, con cuentas de fideicomisos, fiduciantes y fiduciarios en virtud de contratos de fideicomiso.<br>
23. Las personas jurídicas que cumplen funciones de organización y regulación de los deportes profesionales</p>
